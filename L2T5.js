"use strict";
function sumNumbers(a, b) {
    return a + b;
}

function difNumbers(a, b) {
    return a - b;
}

function mulNumbers(a, b) {
    return a * b;
}

function divNumbers(a, b) {
    return a / b;
}

alert(sumNumbers(5, 2));
alert(difNumbers(5, 2));
alert(mulNumbers(5, 2));
alert(divNumbers(5, 2));
